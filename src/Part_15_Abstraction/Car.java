package Part_15_Abstraction;

public interface Car {
	
	//Only static and final variables are defined.
		int wheels = 4;
		
		
		
		//We define only abstract methods
		//No method body.
		//Only method declaration.
		//We achieve 100% abstraction.
		
		public void start();
		public void stop();	
		public void refuel();
		


}
